#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*src;

	i = 0;
	src = (unsigned char *)s;
	while (src[i] && i < n)
	{
		if (src[i] == (unsigned char)c)
			return (src);
		i++;
	}
	return (0);
}
